def hike_segment(
    ladder: PaceLadder,
    pct: int = 70,
    *,
    duration_min: Optional[float] = None,
    distance_km: Optional[float] = None,
    is_beginner: bool = False,
) -> WorkoutSegment:
    """Walking/hiking interval for beginners and ultra athletes."""
    pace = ladder.pace_for_pct(pct) * 1.3  # hike pace is ~30% slower than easy run
    return WorkoutSegment(
        type="hike",
        pace_pct_mp=pct,
        pace_sec_per_km=pace,
        duration_min=duration_min,
        distance_km=distance_km,
        description=describe_segment(pct, pace, is_beginner=is_beginner),
    )


def build_segments(
    workout_type: str,
    ladder: PaceLadder,
    params: dict,
) -> List[WorkoutSegment]:
    """Build a segment list for a given workout type.

    This is the general-purpose entry point.  Phase 2 will add workout-
    specific builders in workout_library.py that call the segment
    primitives above directly.  For now, this supports basic structures.

    Args:
        workout_type: Registered workout type key.
        ladder: The athlete's pace ladder.
        params: Workout-specific parameters (reps, distance, pct, etc).

    Returns:
        List of WorkoutSegment objects.
    """
    is_beginner = params.get("is_beginner", False)

    if workout_type == "easy":
        pct = params.get("pct", 80)
        pace = _resolve_pace(ladder, pct, zone="easy")
        return [
            WorkoutSegment(
                type="easy",
                pace_pct_mp=pct,
                pace_sec_per_km=pace,
                distance_km=params.get("distance_km"),
                description=describe_segment(pct, pace, is_beginner=is_beginner),
            )
        ]

    if workout_type == "easy_strides":
        pct = params.get("pct", 80)
        pace = _resolve_pace(ladder, pct, zone="easy")
        return [
            WorkoutSegment(
                type="easy",
                pace_pct_mp=pct,
                pace_sec_per_km=pace,
                distance_km=params.get("distance_km"),
                description=describe_segment(pct, pace, is_beginner=is_beginner),
            ),
            stride_segment(ladder, reps=params.get("stride_reps", 6), is_beginner=is_beginner),
        ]

    if workout_type == "threshold_cruise":
        return _build_threshold_cruise(ladder, params, is_beginner)

    if workout_type == "vo2max_track":
        return _build_vo2max_track(ladder, params, is_beginner)

    return []


def _build_threshold_cruise(
    ladder: PaceLadder,
    params: dict,
    is_beginner: bool,
) -> List[WorkoutSegment]:
    """Flat tempo/cruise intervals at threshold effort."""
    reps = params.get("reps", 3)
    rep_duration = params.get("rep_duration_min", 10.0)
    rest = params.get("rest_min", 2.0)
    pct = params.get("pct", 105)

    segments: List[WorkoutSegment] = [warmup(ladder, is_beginner=is_beginner)]
    for i in range(reps):
        segments.append(work_segment(
            ladder, pct, zone="threshold",
            duration_min=rep_duration, is_beginner=is_beginner,
        ))
        if i < reps - 1:
            segments.append(jog_rest(ladder, rest, is_beginner=is_beginner))
    segments.append(cooldown(ladder, is_beginner=is_beginner))
    return segments


def _build_vo2max_track(
    ladder: PaceLadder,
    params: dict,
    is_beginner: bool,
) -> List[WorkoutSegment]:
    """Track intervals at 5K-effort."""
    reps = params.get("reps", 5)
    rep_distance = params.get("rep_distance_km", 1.0)
    rest = params.get("rest_min", 3.0)
    pct = params.get("pct", 115)

    segments: List[WorkoutSegment] = [warmup(ladder, is_beginner=is_beginner)]
    for i in range(reps):
        segments.append(work_segment(
            ladder, pct, zone="interval",
            distance_km=rep_distance, is_beginner=is_beginner,
        ))
        if i < reps - 1:
            segments.append(jog_rest(ladder, rest, is_beginner=is_beginner))
    segments.append(cooldown(ladder, is_beginner=is_beginner))
    return segments
