"""
Segments Builder — populates unified segment schema on workouts.

Every structured workout MUST have segments populated.
Easy and rest days may have null segments.

Segment types: warmup, cooldown, work, float, jog_rest, easy,
threshold, interval, stride, steady, hike, fatigue_resistance, uphill_tm.

Every segment includes BOTH pace_pct_mp (internal) AND pace_sec_per_km
(the concrete number).  The effort description is generated by
effort_mapper.describe_segment().

Ref: Algorithm Spec §5 ("Segments Schema").
"""

from __future__ import annotations

from typing import List, Optional

from .effort_mapper import describe_segment
from .models import PaceLadder, WorkoutSegment


def _resolve_pace(ladder: PaceLadder, pct: int, zone: str | None = None) -> float:
    """Resolve pace: use named Daniels zone if specified, otherwise arithmetic.

    This is the one place that decides whether a segment gets the Daniels
    value or the arithmetic ladder value.  When a workout is conceptually
    at "easy" effort, pass zone="easy" and the Daniels easy pace wins.
    When it's at an arbitrary percentage (e.g. 90%), pass zone=None.
    """
    _ZONE_MAP = {
        "easy": ladder.easy,
        "long": ladder.long,
        "marathon": ladder.marathon,
        "threshold": ladder.threshold,
        "interval": ladder.interval,
        "repetition": ladder.repetition,
        "recovery": ladder.recovery,
    }
    if zone and zone in _ZONE_MAP:
        return _ZONE_MAP[zone]
    return ladder.pace_for_pct(pct)


def warmup(
    ladder: PaceLadder,
    distance_km: float = 2.0,
    pct: int = 80,
    *,
    is_beginner: bool = False,
) -> WorkoutSegment:
    pace = _resolve_pace(ladder, pct, zone="easy")
    return WorkoutSegment(
        type="warmup",
        pace_pct_mp=pct,
        pace_sec_per_km=pace,
        distance_km=distance_km,
        description=describe_segment(pct, pace, is_beginner=is_beginner),
    )


